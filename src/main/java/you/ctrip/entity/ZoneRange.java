package you.ctrip.entity;

public class ZoneRange {

	public Integer zone;
	public float pointLon;
	public float pointLat;
	
	public Integer getZone() {
		return zone;
	}
	public void setZone(Integer zone) {
		this.zone = zone;
	}
	public float getPointLon() {
		return pointLon;
	}
	public void setPointLon(float pointLon) {
		this.pointLon = pointLon;
	}
	public float getPointLat() {
		return pointLat;
	}
	public void setPointLat(float pointLat) {
		this.pointLat = pointLat;
	}
}
